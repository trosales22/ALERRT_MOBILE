package com.capstone.alerrt_app.classes.beans;

public class UserBean {
    String userID,fullname,emailAddress,password,birthdate,mobileNumber,gender,dateAndTimeRegistered;

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public String getFullname() {
        return fullname;
    }

    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    public String getEmailAddress() {
        return emailAddress;
    }

    public void setEmailAddress(String emailAddress) {
        this.emailAddress = emailAddress;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getBirthdate() {
        return birthdate;
    }

    public void setBirthdate(String birthdate) {
        this.birthdate = birthdate;
    }

    public String getMobileNumber() {
        return mobileNumber;
    }

    public void setMobileNumber(String mobileNumber) {
        this.mobileNumber = mobileNumber;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getDateAndTimeRegistered() {
        return dateAndTimeRegistered;
    }

    public void setDateAndTimeRegistered(String dateAndTimeRegistered) {
        this.dateAndTimeRegistered = dateAndTimeRegistered;
    }
}
